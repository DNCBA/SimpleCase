public class Temp {

  private Integer id;
  private Long falseFlag;
  private Long trueFlag;
  private Long totalFlag;


  public Long getTotalFlag() {
    return totalFlag;
  }

  public void setTotalFlag(Long totalFlag) {
    this.totalFlag = totalFlag;
  }

  public Integer getId() {
    return id;
  }

  public void setId(Integer id) {
    this.id = id;
  }

  public Long getFalseFlag() {
    return falseFlag;
  }

  public void setFalseFlag(Long falseFlag) {
    this.falseFlag = falseFlag;
  }

  public Long getTrueFlag() {
    return trueFlag;
  }

  public void setTrueFlag(Long trueFlag) {
    this.trueFlag = trueFlag;
  }
}
